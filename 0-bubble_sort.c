#include "sort.h"

/**
 * swap_ints - In an array, swap two integers.
 * @a: the initial swapped integer.
 * @b: The next integer to be swapped.
 */
void swap_ints(int *a, int *b)
{
	int tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}

/**
 * bubble_sort - Integer arrays should be sorted in ascending order.
 * @array: Sorting an array of numbers.
 * @size: The array's dimensions.
 *
 * Description: the array is printed after each swap.
 */
void bubble_sort(int *array, size_t size)
{
	size_t i, len = size;
	bool bubbly = false;

	if (array == NULL || size < 2)
		return;

	while (bubbly == false)
	{
		bubbly = true;
		for (i = 0; i < len - 1; i++)
		{
			if (array[i] > array[i + 1])
			{
				swap_ints(array + i, array + i + 1);
				print_array(array, size);
				bubbly = false;
			}
		}
		len--;
	}
}
